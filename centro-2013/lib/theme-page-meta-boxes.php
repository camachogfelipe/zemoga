function centro_page_save_data($post_id) {
	global $page_meta_box;
	if ( isset( $_POST['centro_page_meta_box_nonce'] ) && !wp_verify_nonce($_POST['centro_page_meta_box_nonce'], basename(__FILE__))) {
		return $post_id;
	}
	if (defined('DOING_AUTOSAVE') && DOING_AUTOSAVE) {
		return $post_id;
	}
	if ( isset( $_POST['post_type'] ) && 'page' == $_POST['post_type']) {
		if (!current_user_can('edit_page', $post_id)) {
			return $post_id;
		}
	} elseif (!current_user_can('edit_post', $post_id)) {
		return $post_id;
	}
	foreach ($page_meta_box['fields'] as $field) {
		$old = get_post_meta($post_id, $field['id'], true);
		if ( isset( $_POST[$field['id']] ) )
			$new = $_POST[$field['id']];
		
		if ( isset( $new ) && $new != $old) {
			update_post_meta($post_id, $field['id'], $new);
		} elseif ( isset( $new ) && '' == $new && $old) {
			delete_post_meta($post_id, $field['id'], $old);
		}
	}
}
